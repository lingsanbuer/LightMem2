#!/usr/bin/env bash
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PINCHBENCH_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../../.." && pwd)"

BASELINE_MODEL="${BASELINE_MODEL:-gpt-5.4-mini}"
BASELINE_JUDGE="${BASELINE_JUDGE:-gpt-5.4-mini}"
BASELINE_SUITE="${BASELINE_SUITE:-all}"
BASELINE_PARALLEL="${BASELINE_PARALLEL:-1}"
BASELINE_SESSION_MODE="${BASELINE_SESSION_MODE:-isolated}"
BASELINE_RUNS="${BASELINE_RUNS:-1}"
BASELINE_TIMEOUT_MULTIPLIER="${BASELINE_TIMEOUT_MULTIPLIER:-1.0}"
BASELINE_MAX_TASKS="${BASELINE_MAX_TASKS:-}"
NOHUP_LOG_DIR="${NOHUP_LOG_DIR:-/tmp}"
SOURCE_OPENCLAW_HOME="${PINCHBENCH_SOURCE_OPENCLAW_HOME:-${HOME}}"
BASE_GATEWAY_PORT="${PINCHBENCH_BASELINE_BASE_GATEWAY_PORT:-20989}"
PINCHBENCH_FWS_SHARED_DATA_DIR="${PINCHBENCH_FWS_SHARED_DATA_DIR:-${HOME}/.local/share/fws}"
PINCHBENCH_GWS_SHARED_CONFIG_DIR="${PINCHBENCH_GWS_SHARED_CONFIG_DIR:-${HOME}/.local/share/fws/config}"

mkdir -p "${NOHUP_LOG_DIR}"

TIMESTAMP="$(date +%Y%m%d_%H%M%S)"
OUT_LOG="${NOHUP_LOG_DIR}/pinchbench_baseline_all_${TIMESTAMP}.out"
PID_FILE="${NOHUP_LOG_DIR}/pinchbench_baseline_all_${TIMESTAMP}.pid"
TMP_ROOT="${TMP_ROOT:-/tmp/pinchbench_baseline_${TIMESTAMP}}"
OPENCLAW_HOME_DIR="${TMP_ROOT}/openclaw_home"
OPENCLAW_CFG="${OPENCLAW_HOME_DIR}/.openclaw/openclaw.json"
OPENCLAW_STATE_DIR="${OPENCLAW_HOME_DIR}/.openclaw"
OPENCLAW_PROFILE="pinchbench-baseline-${TIMESTAMP}"
OPENCLAW_GATEWAY_PORT="${OPENCLAW_GATEWAY_PORT:-${BASE_GATEWAY_PORT}}"
FWS_DATA_DIR="${TMP_ROOT}/fws-shared"
GWS_SOURCE_CONFIG_DIR="${TMP_ROOT}/gws-shared-config"

if [[ -d "${TMP_ROOT}" ]]; then
  OPENCLAW_CONFIG_PATH="${OPENCLAW_CFG}" \
  OPENCLAW_STATE_DIR="${OPENCLAW_STATE_DIR}" \
  OPENCLAW_PROFILE="${OPENCLAW_PROFILE}" \
  OPENCLAW_GATEWAY_PORT="${OPENCLAW_GATEWAY_PORT}" \
  openclaw --profile "${OPENCLAW_PROFILE}" gateway stop >/dev/null 2>&1 || true
  rm -rf "${TMP_ROOT}"
fi

mkdir -p "${OPENCLAW_HOME_DIR}" "${FWS_DATA_DIR}" "${GWS_SOURCE_CONFIG_DIR}"

if [[ -d "${PINCHBENCH_FWS_SHARED_DATA_DIR}" ]]; then
  cp -a "${PINCHBENCH_FWS_SHARED_DATA_DIR}/." "${FWS_DATA_DIR}/"
fi
if [[ -d "${PINCHBENCH_GWS_SHARED_CONFIG_DIR}" ]]; then
  cp -a "${PINCHBENCH_GWS_SHARED_CONFIG_DIR}/." "${GWS_SOURCE_CONFIG_DIR}/"
fi

if [[ ! -d "${SOURCE_OPENCLAW_HOME}/.openclaw" ]]; then
  echo "Missing source OpenClaw state dir: ${SOURCE_OPENCLAW_HOME}/.openclaw" >&2
  exit 1
fi

cp -a "${SOURCE_OPENCLAW_HOME}/.openclaw" "${OPENCLAW_HOME_DIR}/.openclaw"
rm -rf "${OPENCLAW_STATE_DIR}/agents"
mkdir -p "${OPENCLAW_STATE_DIR}/agents"
rm -rf \
  "${OPENCLAW_STATE_DIR}/extensions/tokenpilot" \
  "${OPENCLAW_STATE_DIR}/tokenpilot-plugin-state"

cd "${REPO_ROOT}"

nohup bash -lc "
set -euo pipefail
cd '${REPO_ROOT}'
set -a
source '${REPO_ROOT}/experiments/pinchbench/.env'
set +a
PINCHBENCH_TMP_ROOT='${TMP_ROOT}' \\
TOKENPILOT_OPENCLAW_HOME='${OPENCLAW_HOME_DIR}' \\
OPENCLAW_CONFIG_PATH='${OPENCLAW_CFG}' \\
OPENCLAW_STATE_DIR='${OPENCLAW_STATE_DIR}' \\
OPENCLAW_PROFILE='${OPENCLAW_PROFILE}' \\
OPENCLAW_GATEWAY_PORT='${OPENCLAW_GATEWAY_PORT}' \\
FWS_DATA_DIR='${FWS_DATA_DIR}' \\
GWS_SOURCE_CONFIG_DIR='${GWS_SOURCE_CONFIG_DIR}' \\
PINCHBENCH_DISABLE_CANONICAL_STATE_FALLBACK='true' \\
TOKENPILOT_FORCE_GATEWAY_RESTART='true' \\
TOKENPILOT_BASELINE_MODEL='${BASELINE_MODEL}' \\
TOKENPILOT_BASELINE_JUDGE='${BASELINE_JUDGE}' \\
TOKENPILOT_BASELINE_SUITE='${BASELINE_SUITE}' \\
TOKENPILOT_BASELINE_PARALLEL='${BASELINE_PARALLEL}' \\
TOKENPILOT_BASELINE_SESSION_MODE='${BASELINE_SESSION_MODE}' \\
TOKENPILOT_BASELINE_RUNS='${BASELINE_RUNS}' \\
TOKENPILOT_BASELINE_TIMEOUT_MULTIPLIER='${BASELINE_TIMEOUT_MULTIPLIER}' \\
$(if [[ -n "${BASELINE_MAX_TASKS}" ]]; then printf "PINCHBENCH_MAX_TASKS='%s' \\\\\n" "${BASELINE_MAX_TASKS}"; fi)bash '${REPO_ROOT}/experiments/pinchbench/scripts/run_baseline.sh'
" >"${OUT_LOG}" 2>&1 < /dev/null &

PID=$!
echo "${PID}" > "${PID_FILE}"

echo "Started isolated baseline run"
echo "PID: ${PID}"
echo "PID file: ${PID_FILE}"
echo "Log: ${OUT_LOG}"
echo "TMP root: ${TMP_ROOT}"
echo "OPENCLAW_HOME: ${OPENCLAW_HOME_DIR}"
echo "Gateway port: ${OPENCLAW_GATEWAY_PORT}"
