#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"

METHOD_MODEL="${METHOD_MODEL:-tokenpilot/gpt-5.4-mini}"
METHOD_JUDGE="${METHOD_JUDGE:-gpt-5.4-mini}"
METHOD_PARALLEL="${METHOD_PARALLEL:-1}"
CATEGORY="${CATEGORY:-workflow}"
MAX_TASKS="${MAX_TASKS:-6}"
NOHUP_LOG_DIR="${NOHUP_LOG_DIR:-/tmp}"

ESTIMATOR_API_KEY="${TOKENPILOT_TASK_STATE_ESTIMATOR_API_KEY:-sk-fZ0W1sYG9u2zeY2wkp2P7XYtgQg1NsG9Ldp1Y40xniIX6VAN}"
ESTIMATOR_BASE_URL="${TOKENPILOT_TASK_STATE_ESTIMATOR_BASE_URL:-https://www.dmxapi.cn/v1}"
ESTIMATOR_MODEL="${TOKENPILOT_TASK_STATE_ESTIMATOR_MODEL:-qwen3.5-35b-a3b}"
ESTIMATOR_BATCH_TURNS="${TOKENPILOT_TASK_STATE_ESTIMATOR_BATCH_TURNS:-1}"
ESTIMATOR_INPUT_MODE="${TOKENPILOT_TASK_STATE_ESTIMATOR_INPUT_MODE:-completed_summary_plus_active_turns}"
ESTIMATOR_LIFECYCLE_MODE="${TOKENPILOT_TASK_STATE_ESTIMATOR_LIFECYCLE_MODE:-decoupled}"
ESTIMATOR_EVICTION_PROMOTION_POLICY="${TOKENPILOT_TASK_STATE_ESTIMATOR_EVICTION_PROMOTION_POLICY:-fifo}"
ESTIMATOR_EVICTION_PROMOTION_HOT_TAIL_SIZE="${TOKENPILOT_TASK_STATE_ESTIMATOR_EVICTION_PROMOTION_HOT_TAIL_SIZE:-1}"

mkdir -p "${NOHUP_LOG_DIR}"

TIMESTAMP="$(date +%Y%m%d_%H%M%S)"
OUT_LOG="${NOHUP_LOG_DIR}/claw_eval_continuous_full_on_${CATEGORY}_${TIMESTAMP}.out"
PID_FILE="${NOHUP_LOG_DIR}/claw_eval_continuous_full_on_${CATEGORY}_${TIMESTAMP}.pid"

cd "${REPO_ROOT}"

INNER_SCRIPT="$(mktemp /tmp/claw_eval_cont_full_on_XXXXXX.sh)"
cat > "${INNER_SCRIPT}" <<EOF
#!/usr/bin/env bash
set -euo pipefail
cd '${REPO_ROOT}'
set -a
if [[ -f '${REPO_ROOT}/experiments/tokenpilot/claw-eval/.env' ]]; then
  source '${REPO_ROOT}/experiments/tokenpilot/claw-eval/.env'
fi
if [[ -f '${REPO_ROOT}/experiments/tokenpilot/pinchbench/.env' ]]; then
  source '${REPO_ROOT}/experiments/tokenpilot/pinchbench/.env'
fi
set +a

export TOKENPILOT_TASK_STATE_ESTIMATOR_API_KEY='${ESTIMATOR_API_KEY}'
export TOKENPILOT_TASK_STATE_ESTIMATOR_BASE_URL='${ESTIMATOR_BASE_URL}'
export TOKENPILOT_TASK_STATE_ESTIMATOR_MODEL='${ESTIMATOR_MODEL}'
export TOKENPILOT_TASK_STATE_ESTIMATOR_BATCH_TURNS='${ESTIMATOR_BATCH_TURNS}'
export TOKENPILOT_TASK_STATE_ESTIMATOR_INPUT_MODE='${ESTIMATOR_INPUT_MODE}'
export TOKENPILOT_TASK_STATE_ESTIMATOR_LIFECYCLE_MODE='${ESTIMATOR_LIFECYCLE_MODE}'
export TOKENPILOT_TASK_STATE_ESTIMATOR_EVICTION_PROMOTION_POLICY='${ESTIMATOR_EVICTION_PROMOTION_POLICY}'
export TOKENPILOT_TASK_STATE_ESTIMATOR_EVICTION_PROMOTION_HOT_TAIL_SIZE='${ESTIMATOR_EVICTION_PROMOTION_HOT_TAIL_SIZE}'

export TOKENPILOT_RUNTIME_ENABLED='true'
export TOKENPILOT_ENABLE_REDUCTION='true'
export TOKENPILOT_ENABLE_EVICTION='true'
export TOKENPILOT_TASK_STATE_ESTIMATOR_ENABLED='true'
export TOKENPILOT_MEMORY_ENABLED='false'
export TOKENPILOT_MEMORY_AUTO_DISTILL='false'
export TOKENPILOT_MEMORY_INJECT_AS_SYSTEM_HINT='false'
export TOKENPILOT_MEMORY_TOP_K='0'

export TOKENPILOT_REDUCTION_PASS_REPEATED_READ_DEDUP='true'
export TOKENPILOT_REDUCTION_PASS_TOOL_PAYLOAD_TRIM='true'
export TOKENPILOT_REDUCTION_PASS_HTML_SLIMMING='true'
export TOKENPILOT_REDUCTION_PASS_EXEC_OUTPUT_TRUNCATION='true'
export TOKENPILOT_REDUCTION_PASS_AGENTS_STARTUP_OPTIMIZATION='true'
export TOKENPILOT_REDUCTION_PASS_FORMAT_SLIMMING='true'
export TOKENPILOT_REDUCTION_PASS_FORMAT_CLEANING='true'
export TOKENPILOT_REDUCTION_PASS_PATH_TRUNCATION='true'
export TOKENPILOT_REDUCTION_PASS_IMAGE_DOWNSAMPLE='true'
export TOKENPILOT_REDUCTION_PASS_LINE_NUMBER_STRIP='true'

exec bash '${REPO_ROOT}/experiments/tokenpilot/claw-eval/scripts/run_method.sh' \
  --scope category \
  --category '${CATEGORY}' \
  --session-mode continuous \
  --profile plugin \
  --tmp-openclaw \
  --parallel '${METHOD_PARALLEL}' \
  --max-tasks '${MAX_TASKS}' \
  --model '${METHOD_MODEL}' \
  --judge '${METHOD_JUDGE}'
EOF

chmod +x "${INNER_SCRIPT}"

nohup bash "${INNER_SCRIPT}" >"${OUT_LOG}" 2>&1 < /dev/null &

PID=$!
echo "${PID}" > "${PID_FILE}"

echo "Started claw-eval continuous full-on run"
echo "Category: ${CATEGORY}"
echo "Max tasks: ${MAX_TASKS}"
echo "PID: ${PID}"
echo "PID file: ${PID_FILE}"
echo "Log: ${OUT_LOG}"
