#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"

CONTINUOUS_MODEL="${CONTINUOUS_MODEL:-gpt-5.4-mini}"
CONTINUOUS_JUDGE="${CONTINUOUS_JUDGE:-gpt-5.4-mini}"
CONTINUOUS_RUNS="${CONTINUOUS_RUNS:-1}"
CONTINUOUS_PARALLEL="${CONTINUOUS_PARALLEL:-1}"
CONTINUOUS_CATEGORY_FILTER="${CONTINUOUS_CATEGORY_FILTER:-csv_analysis,integrations}"

ISOLATED_MODEL="${ISOLATED_MODEL:-tokenpilot/gpt-5.4-mini}"
ISOLATED_JUDGE="${ISOLATED_JUDGE:-gpt-5.4-mini}"
ISOLATED_RUNS="${ISOLATED_RUNS:-1}"
ISOLATED_PARALLEL="${ISOLATED_PARALLEL:-1}"
ISOLATED_SUITE="${ISOLATED_SUITE:-all}"
ISOLATED_VARIANTS="${ISOLATED_VARIANTS:-with_reduction_pass}"
TRANSCRIPT_MEMO_MIN_CALLS="${TRANSCRIPT_MEMO_MIN_CALLS:-4}"

cd "${REPO_ROOT}"
set -a
source "${REPO_ROOT}/experiments/tokenpilot/pinchbench/.env"
set +a

echo "[chain] step=1 continuous categories=${CONTINUOUS_CATEGORY_FILTER}"
TOKENPILOT_SESSION_MODE=continuous \
PINCHBENCH_METHOD_MODEL="${CONTINUOUS_MODEL}" \
PINCHBENCH_METHOD_JUDGE="${CONTINUOUS_JUDGE}" \
PINCHBENCH_CATEGORY_FILTER="${CONTINUOUS_CATEGORY_FILTER}" \
TOKENPILOT_ENABLE_REDUCTION=true \
TOKENPILOT_ENABLE_EVICTION=false \
TOKENPILOT_TASK_STATE_ESTIMATOR_ENABLED=false \
TOKENPILOT_MEMORY_ENABLED=false \
TOKENPILOT_REDUCTION_PASS_REPEATED_READ_DEDUP=true \
TOKENPILOT_REDUCTION_PASS_TOOL_PAYLOAD_TRIM=true \
TOKENPILOT_REDUCTION_PASS_HTML_SLIMMING=true \
TOKENPILOT_REDUCTION_PASS_EXEC_OUTPUT_TRUNCATION=true \
TOKENPILOT_REDUCTION_PASS_AGENTS_STARTUP_OPTIMIZATION=true \
TOKENPILOT_REDUCTION_PASS_FORMAT_SLIMMING=true \
TOKENPILOT_REDUCTION_PASS_FORMAT_CLEANING=true \
TOKENPILOT_REDUCTION_PASS_PATH_TRUNCATION=true \
TOKENPILOT_REDUCTION_PASS_IMAGE_DOWNSAMPLE=true \
TOKENPILOT_REDUCTION_PASS_LINE_NUMBER_STRIP=true \
TOKENPILOT_TRANSCRIPT_MEMO_MIN_CALLS="${TRANSCRIPT_MEMO_MIN_CALLS}" \
TOKENPILOT_RUNS="${CONTINUOUS_RUNS}" \
TOKENPILOT_PARALLEL="${CONTINUOUS_PARALLEL}" \
"${REPO_ROOT}/experiments/tokenpilot/pinchbench/scripts/run_method_by_category_continuous.sh"

echo "[chain] step=2 isolated suite=${ISOLATED_SUITE} variants=${ISOLATED_VARIANTS}"
PINCHBENCH_ISOLATED_ABLATION_VARIANTS="${ISOLATED_VARIANTS}" \
PINCHBENCH_METHOD_MODEL="${ISOLATED_MODEL}" \
PINCHBENCH_METHOD_JUDGE="${ISOLATED_JUDGE}" \
PINCHBENCH_METHOD_SUITE="${ISOLATED_SUITE}" \
TOKENPILOT_TRANSCRIPT_MEMO_MIN_CALLS="${TRANSCRIPT_MEMO_MIN_CALLS}" \
TOKENPILOT_RUNS="${ISOLATED_RUNS}" \
TOKENPILOT_PARALLEL="${ISOLATED_PARALLEL}" \
TOKENPILOT_TIMEOUT_MULTIPLIER=1.0 \
"${REPO_ROOT}/experiments/tokenpilot/pinchbench/scripts/run_method_isolated_reduction_ablation.sh"

echo "[chain] completed"
