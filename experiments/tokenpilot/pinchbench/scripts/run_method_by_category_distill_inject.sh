#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

export PINCHBENCH_TMP_ROOT="${PINCHBENCH_TMP_ROOT:-/tmp/pinchbench_by_category_distill_inject}"

export TOKENPILOT_SESSION_MODE="${TOKENPILOT_SESSION_MODE:-continuous}"

# Keep reduction framework active, but disable concrete passes for this run.
export TOKENPILOT_ENABLE_REDUCTION="${TOKENPILOT_ENABLE_REDUCTION:-true}"
export TOKENPILOT_REDUCTION_PASS_REPEATED_READ_DEDUP="${TOKENPILOT_REDUCTION_PASS_REPEATED_READ_DEDUP:-false}"
export TOKENPILOT_REDUCTION_PASS_TOOL_PAYLOAD_TRIM="${TOKENPILOT_REDUCTION_PASS_TOOL_PAYLOAD_TRIM:-false}"
export TOKENPILOT_REDUCTION_PASS_HTML_SLIMMING="${TOKENPILOT_REDUCTION_PASS_HTML_SLIMMING:-false}"
export TOKENPILOT_REDUCTION_PASS_EXEC_OUTPUT_TRUNCATION="${TOKENPILOT_REDUCTION_PASS_EXEC_OUTPUT_TRUNCATION:-false}"
export TOKENPILOT_REDUCTION_PASS_AGENTS_STARTUP_OPTIMIZATION="${TOKENPILOT_REDUCTION_PASS_AGENTS_STARTUP_OPTIMIZATION:-false}"
export TOKENPILOT_REDUCTION_PASS_FORMAT_SLIMMING="${TOKENPILOT_REDUCTION_PASS_FORMAT_SLIMMING:-false}"
export TOKENPILOT_REDUCTION_PASS_FORMAT_CLEANING="${TOKENPILOT_REDUCTION_PASS_FORMAT_CLEANING:-false}"
export TOKENPILOT_REDUCTION_PASS_PATH_TRUNCATION="${TOKENPILOT_REDUCTION_PASS_PATH_TRUNCATION:-false}"
export TOKENPILOT_REDUCTION_PASS_IMAGE_DOWNSAMPLE="${TOKENPILOT_REDUCTION_PASS_IMAGE_DOWNSAMPLE:-false}"
export TOKENPILOT_REDUCTION_PASS_LINE_NUMBER_STRIP="${TOKENPILOT_REDUCTION_PASS_LINE_NUMBER_STRIP:-false}"

# Enable estimator + eviction + distill.
export TOKENPILOT_ENABLE_EVICTION="${TOKENPILOT_ENABLE_EVICTION:-true}"
export TOKENPILOT_MEMORY_ENABLED="${TOKENPILOT_MEMORY_ENABLED:-true}"
export TOKENPILOT_MEMORY_AUTO_DISTILL="${TOKENPILOT_MEMORY_AUTO_DISTILL:-true}"
export TOKENPILOT_MEMORY_DISTILLER_TYPE="${TOKENPILOT_MEMORY_DISTILLER_TYPE:-prompting}"
export TOKENPILOT_MEMORY_BATCH_SIZE="${TOKENPILOT_MEMORY_BATCH_SIZE:-2}"

# Injection-on default. Mirror the previously successful smoke defaults unless overridden.
export TOKENPILOT_MEMORY_TOP_K="${TOKENPILOT_MEMORY_TOP_K:-1}"
export TOKENPILOT_MEMORY_INJECT_AS_SYSTEM_HINT="${TOKENPILOT_MEMORY_INJECT_AS_SYSTEM_HINT:-false}"

# Use the current preferred estimator backend unless caller overrides.
export TOKENPILOT_TASK_STATE_ESTIMATOR_BASE_URL="${TOKENPILOT_TASK_STATE_ESTIMATOR_BASE_URL:-https://kuaipao.ai/v1}"
export TOKENPILOT_TASK_STATE_ESTIMATOR_MODEL="${TOKENPILOT_TASK_STATE_ESTIMATOR_MODEL:-gpt-5.4-mini}"
export TOKENPILOT_TASK_STATE_ESTIMATOR_BATCH_TURNS="${TOKENPILOT_TASK_STATE_ESTIMATOR_BATCH_TURNS:-1}"

# Keep distill provider aligned with the estimator provider by default, matching smoke runs.
export TOKENPILOT_MEMORY_DISTILL_BASE_URL="${TOKENPILOT_MEMORY_DISTILL_BASE_URL:-${TOKENPILOT_TASK_STATE_ESTIMATOR_BASE_URL:-}}"
export TOKENPILOT_MEMORY_DISTILL_API_KEY="${TOKENPILOT_MEMORY_DISTILL_API_KEY:-${TOKENPILOT_TASK_STATE_ESTIMATOR_API_KEY:-}}"
export TOKENPILOT_MEMORY_DISTILL_MODEL="${TOKENPILOT_MEMORY_DISTILL_MODEL:-${TOKENPILOT_TASK_STATE_ESTIMATOR_MODEL:-}}"
export TOKENPILOT_MEMORY_DISTILL_TIMEOUT_MS="${TOKENPILOT_MEMORY_DISTILL_TIMEOUT_MS:-600000}"

exec "${SCRIPT_DIR}/run_method_by_category_continuous.sh"
